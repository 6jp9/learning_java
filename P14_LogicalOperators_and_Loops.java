public class P14_LogicalOperators_and_Loops {
    public static void main(String[] args) {
        
        // && --> And
        // || --> Or
        // !  --> Not
        // ####################

        // While loops
        // syntax : while(condition){  block  };

        int a=1;
        while (a<=10) {
            System.out.println(a);
            a++;
        }

        // for loop
        // syntax: for(counter; condition; step(or increment)){    block   };

        for(int i=1; i<=10 ; i++){
            System.out.println(i);
        };

    }
}
